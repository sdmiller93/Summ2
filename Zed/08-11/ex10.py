# the \t indents the line or "tabs" in
tabby_cat = "\tI'm tabbed in."

# the \n puts it on a new line
persian_cat = "I'm split\non a line."

# \\ a \\ types it like this --> \ a \ 
backslash_cat = "I'm \\ a \\ cat."

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)

